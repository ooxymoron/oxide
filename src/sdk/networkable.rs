use std::ffi::CStr;

use crate::{vmt_call, cfn, sdk::*};

#[repr(C)]
#[derive(Debug, Clone)]
pub struct UnparsedClientClass {
    _pad1: [i32; 2],
    pub network_name: *const char,
    _pad2: [i32; 2],
    pub class_id: ClassId,
}

#[repr(C)]
#[derive(Debug, Clone)]
pub struct ClientClass {
    pub network_name: String,
    pub class_id: ClassId,
}

#[repr(C)]
#[derive(Derivative, Clone)]
#[derivative(Debug)]
pub struct VMTNetworkable {
    _pad1: [i32; 2],
    get_client_class: cfn!(&'static UnparsedClientClass, *const Networkable),
    _pad2: [i32; 5],
    pub is_dormant: cfn!(bool, *const Networkable),
    pub get_index: cfn!(isize, *const Networkable),
}

pub type Networkable = WithVmt<VMTNetworkable>;

impl Networkable {
    pub fn get_client_class(&self) -> ClientClass {
        let client_class = vmt_call!(self, get_client_class);
        let network_name = unsafe {
            CStr::from_ptr(client_class.network_name as *const i8)
                .to_str().unwrap()
                .to_owned()
        };
        ClientClass {
            network_name,
            class_id: client_class.class_id,
        }
    }
}

